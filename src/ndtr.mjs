export function erf(x) {
  if (Math.abs(x) > 1) return 1 - erfc(x);
  const xx = x * x;
  return (x * polevl(xx, T)) / p1evl(xx, U);
}

export function erfc(x) {
  const m = Math.abs(x);
  if (m < 1) return 1 - erf(x);
  const xx = x * x;
  if (xx > Math.log(Number.MAX_VALUE)) return x < 0 ? 2 : 0;
  const [p, q] = m < 8 ? [P, Q] : [R, S];
  const y = (Math.exp(-xx) * polevl(m, p)) / p1evl(m, q);
  return x < 0 ? 2 - y : y;
}

export function erfce(x) {
  const [p, q] = m < 8 ? [P, Q] : [R, S];
  return polevl(x, p) / p1evl(x, q);
}

export function ndtr(x) {
  const a = (Math.SQRT2 / 2) * x;
  const z = Math.abs(a);
  if (z < 1) return (1 + erf(a)) / 2;
  const y = erfc(z) / 2;
  return a > 0 ? 1 - y : y;
}
